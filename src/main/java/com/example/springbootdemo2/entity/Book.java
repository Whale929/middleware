package com.example.springbootdemo2.entity;

public class Book {
    private Integer bookNo;
    private String name;

    public Integer getBookNo() {
        return bookNo;
    }

    public String getName() {
        return name;
    }

    public void setBookNo(Integer bookNo) {
        this.bookNo = bookNo;
    }

    public void setName(String name) {
        this.name = name;
    }
}
